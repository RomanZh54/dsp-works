Fs = 1e3;                       % Частота дискретизації
t = -50e-3 : 1 / Fs : 50e-3;    % Дискретний час
A = 10;                         % Амплітуда
T1 = 0.5;                       % Зсув вершини
T2 = 60e-3;                     % Нижня основа
s = A * tripuls(t, T2, T1);     % Генерація імпульсу
plot(t, s)
grid on

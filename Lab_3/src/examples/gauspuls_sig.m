Fs = 16e3;                      % Частота дискретизації 16 кГц
t = -10e-3 : 1 / Fs : 10e-3;    % Довжина радіоімпульсу
Fc = 4e3;                       % Опорна частота 4 кГц
bw = 0.1;                       % Відносна ширина спектру 0.1
bwr = -20;                      % Рівень = -20 дБ
s = gauspuls(t, Fc, bw, bwr);   % Генерація радіоімпульсу
plot(t, s);
grid on
